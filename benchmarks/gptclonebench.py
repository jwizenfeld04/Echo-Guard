"""GPTCloneBench benchmark adapter.

GPTCloneBench (Alam et al., 2024) is a benchmark of AI-generated clone pairs,
specifically targeting code generated by large language models like GPT.

This is particularly relevant for Echo Guard since its primary use case is
detecting redundancy created by AI coding agents.

Dataset contains Python and Java pairs across all clone types, with a focus
on the kinds of duplicates that LLMs typically generate.

Reference: https://github.com/AluaBa662/GPTCloneBench
"""

from __future__ import annotations

import csv
from pathlib import Path

from benchmarks.base import BenchmarkAdapter, BenchmarkPair


class GPTCloneBenchAdapter(BenchmarkAdapter):
    """Adapter for the GPTCloneBench dataset.

    Supports:
    1. `curated` — Built-in representative AI-generated pairs (no download needed)
    2. `full` — All pairs from the dataset (requires download)
    """

    @property
    def name(self) -> str:
        return "GPTCloneBench"

    @property
    def dataset_id(self) -> str:
        return "gptclonebench"

    def is_available(self) -> bool:
        gcb_dir = self.data_dir / "gptclonebench"
        return (gcb_dir / "clone_pairs.csv").exists() or True  # Curated always available

    def download(self, force: bool = False) -> None:
        gcb_dir = self.data_dir / "gptclonebench"
        if gcb_dir.exists() and not force:
            print(f"  GPTCloneBench data already exists at {gcb_dir}")
            return

        gcb_dir.mkdir(parents=True, exist_ok=True)

        print("  Downloading GPTCloneBench dataset...")
        print("  Note: Full dataset requires manual download from:")
        print("    https://github.com/AluaBa662/GPTCloneBench")
        print()
        print("  For now, using built-in curated AI-generated pairs.")
        print("  To use the full dataset, clone the GPTCloneBench repo into:")
        print(f"    {gcb_dir}")

    def load_pairs(self, max_pairs: int | None = None) -> list[BenchmarkPair]:
        gcb_dir = self.data_dir / "gptclonebench"
        clone_csv = gcb_dir / "clone_pairs.csv"

        if clone_csv.exists():
            return self._load_from_csv(clone_csv, max_pairs)

        return self._load_curated_pairs(max_pairs)

    def _load_from_csv(
        self, csv_path: Path, max_pairs: int | None
    ) -> list[BenchmarkPair]:
        """Load pairs from GPTCloneBench CSV format."""
        pairs: list[BenchmarkPair] = []
        source_dir = csv_path.parent / "functions"

        with open(csv_path, "r") as f:
            reader = csv.DictReader(f)
            for row in reader:
                if max_pairs and len(pairs) >= max_pairs:
                    break

                func_id_1 = row.get("function_id_1", "")
                func_id_2 = row.get("function_id_2", "")
                clone_type = row.get("clone_type", "").lower()
                language = row.get("language", "python").lower()
                is_clone = row.get("is_clone", "true").lower() == "true"

                # Normalize clone type naming
                type_map = {
                    "t1": "type1", "type-1": "type1", "type1": "type1",
                    "t2": "type2", "type-2": "type2", "type2": "type2",
                    "t3": "type3", "type-3": "type3", "type3": "type3",
                    "t4": "type4", "type-4": "type4", "type4": "type4",
                    "st3": "type3", "vst3": "type3",  # BCB subtypes
                    "false": "negative",
                }
                clone_type = type_map.get(clone_type, clone_type)

                code_a = self._load_source(source_dir, func_id_1, language)
                code_b = self._load_source(source_dir, func_id_2, language)

                if code_a is None or code_b is None:
                    continue

                pairs.append(
                    BenchmarkPair(
                        pair_id=f"gcb_{func_id_1}_{func_id_2}",
                        code_a=code_a,
                        code_b=code_b,
                        language_a=language,
                        language_b=language,
                        is_clone=is_clone,
                        clone_type=clone_type,
                        source_dataset="gptclonebench",
                        metadata={
                            "function_id_1": func_id_1,
                            "function_id_2": func_id_2,
                            "generated_by": "gpt",
                        },
                    )
                )

        return pairs

    def _load_source(self, source_dir: Path, func_id: str, language: str) -> str | None:
        ext = "py" if language == "python" else "java"
        path = source_dir / f"{func_id}.{ext}"
        if path.exists():
            return path.read_text(errors="replace")
        return None

    def _load_curated_pairs(
        self, max_pairs: int | None
    ) -> list[BenchmarkPair]:
        """Built-in curated AI-generated clone pairs.

        These simulate the kinds of duplicates that GPT/Claude/Copilot typically
        generate: same problem, different phrasing, slightly different approaches.
        Focus on Python and Java as in the original GPTCloneBench.
        """
        pairs = _CURATED_GPT_PAIRS[:]
        if max_pairs:
            pairs = pairs[:max_pairs]
        return pairs


